public class LesBoucles1 {
    public static void main(String[] args) {
        // while boucle
        int i = 0;
        while (i<10) {
            System.out.println(i);
            i++;
        }


        // do while boucle
        int y = 0;
        do {
            System.out.print(y);
            y++;
        }
        while (y<10);

        //for loop
        for (int z=0; z<10; z+=1){
            System.out.println(z);
        }

    }
}
